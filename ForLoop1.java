package javahomework3;

public class ForLoop1 {
    public static void main(String[] args) {
       // Even number
        System.out.println("Even Numbers");
        for(int i=1;i<=20;i++){
            if(i%2 == 0){
                System.out.println(i);
            }

        }
        // Odd number
        System.out.println("Even Numbers");
        for(int i=1;i<=20;i++){
            if(i%2 != 0){
                System.out.println(i);
            }

        }
    }

}
